package treasuremap.common.general;

public class BusinessEntity {
	private Credit mCredit;
	private MemberInfo mMemberInfo;
	private Tag mTag;
	private UserInfo mUserInfo;
	
	public Credit getmCredit() {
		return mCredit;
	}
	public void setmCredit(Credit mCredit) {
		this.mCredit = mCredit;
	}
	public MemberInfo getmMemberInfo() {
		return mMemberInfo;
	}
	public void setmMemberInfo(MemberInfo mMemberInfo) {
		this.mMemberInfo = mMemberInfo;
	}
	public Tag getmTag() {
		return mTag;
	}
	public void setmTag(Tag mTag) {
		this.mTag = mTag;
	}
	public UserInfo getmUserInfo() {
		return mUserInfo;
	}
	public void setmUserInfo(UserInfo mUserInfo) {
		this.mUserInfo = mUserInfo;
	}
	
}
